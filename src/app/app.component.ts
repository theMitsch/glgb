export class AppComponent implements OnInit {
  dietharz = new Dietharz();
  tammbach = new Tammbach();
  trys = 0;
  combinations = this.dietharz.names.length * this.tammbach.names.length;
  tammtammDingsbumms = '';

  ngOnInit() {
    this.generateNewCombination();
  }

  generateNewCombination() {
    this.trys = this.trys + 1;
    this.tammtammDingsbumms = this.getRandomWordFromList(this.tammbach.names) + ' - ' + this.getRandomWordFromList(this.dietharz.names);
  }
  private getRandomWordFromList(words: string[]): string {
    return words[Math.floor(Math.random() * words.length)];
  }

}

